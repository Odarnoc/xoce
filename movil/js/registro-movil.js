$("#nameregistro-movil").keyup(function () {

  var nameregistro = $("#nameregistro-movil").val();
  if (nameregistro == "") {

    $("#labelnameregistro").css("color", "#F4F4F4");
    $("#nameregistro-movil").attr("placeholder", "Nombre");

  } else {
    $("#labelnameregistro").css("color", "rgba(0,0,0,.5)");
  }

});


